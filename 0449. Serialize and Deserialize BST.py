        def dfs(min_val, max_val):
            if vals and min_val < vals[0] < max_val:
                val = vals.popleft()
                node = TreeNode(val)
                node.left = dfs(min_val, val)
                node.right = dfs(val, max_val)
                return node

        return dfs(float('-inf'), float('inf'))

